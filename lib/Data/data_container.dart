const String DATABASE_NAME = "heroshandbook";

const String CHARACTER_TABLE_NAME = 'character';
const String CHARACTER_COLUMN_ID = 'id_character';
const String CHARACTER_COLUMN_NAME = 'st_name';
const String CHARACTER_COLUMN_CLASS = 'st_class';
const String CHARACTER_COLUMN_IMAGE = 'image';

final String CREATE_CHARACTER_TABLE_SCRIPT = '''
CREATE TABLE $CHARACTER_TABLE_NAME(
  $CHARACTER_COLUMN_ID INTEGER PRIMARY KEY AUTOINCREMENT,
  $CHARACTER_COLUMN_NAME TEXT,
  $CHARACTER_COLUMN_CLASS TEXT,
  $CHARACTER_COLUMN_IMAGE TEXT
)
''';

const String MAGIC_TABLE_NAME = 'magic';
const String MAGIC_COLUMN_ID = 'id_magic';
const String MAGIC_COLUMN_NAME = 'st_name';
const String MAGIC_COLUMN_TYPE = 'st_type';
const String MAGIC_COLUMN_DESCRIPTION = 'st_description';
const String MAGIC_COLUMN_CHARACTER_ID = 'id_character';

const String CREATE_MAGIC_TABLE_SCRIPT = '''
CREATE TABLE $MAGIC_TABLE_NAME(
  $MAGIC_COLUMN_ID INTEGER PRIMARY KEY AUTOINCREMENT,
  $MAGIC_COLUMN_NAME TEXT,
  $MAGIC_COLUMN_TYPE TEXT,
  $MAGIC_COLUMN_DESCRIPTION TEXT,
  $MAGIC_COLUMN_CHARACTER_ID INTEGER,
  FOREIGN KEY ($MAGIC_COLUMN_CHARACTER_ID) REFERENCES $CHARACTER_TABLE_NAME($CHARACTER_COLUMN_ID)
)
''';
